#include "libft.h"

int		ft_atoi(const char *str)
{
	int		i;
	int		n;
	int		sum;

	i = 0;
	n = 0;
	sum = 0;
	while (str[i] == ' ' || str[i] == '\n' || str[i] == '\t'
			|| str[i] == '\v' || str[i] == '\f' || str[i] == '\r')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			n = 1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		sum = ((sum * 10) + (str[i] - '0'));
		i++;
	}
	if (n == 1)
		return (-sum);
	return (sum);
}
