#include "libft.h"

void	ft_putnbr(int nb)
{
	unsigned int	nbr;

	nbr = 0;
	if (nb < 0)
	{
		ft_putchar('-');
	}
	nbr = (nb < 0 ? -nb : nb);
	if (nbr >= 10)
	{
		ft_putnbr(nbr / 10);
		ft_putnbr(nbr % 10);
	}
	else
		ft_putchar(nbr + '0');
}
